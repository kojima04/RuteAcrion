//=============================================================
// DrawObject.h
// 描画オブジェクトの基底クラスの定義
//=============================================================
#pragma once

//=============================================================
// include
//=============================================================
#include <d3d9.h>
#include <d3dx9.h>

//=============================================================
// DrawObject
// 描画オブジェクトの基底クラスの定義
//
// このクラスを描画するオブジェクトに継承する
//=============================================================
class DrawObject
{
public:
	//---------------------------------------------------------
	// コンストラクタ
	//---------------------------------------------------------
	DrawObject(){};

	//---------------------------------------------------------
	// 仮想デストラクタ
	//---------------------------------------------------------
	virtual ~DrawObject(){};


	
	//---------------------------------------------------------
	// オブジェクトの生成
	//---------------------------------------------------------
	virtual HRESULT Create() = 0;
	//---------------------------------------------------------
	// 描画する
	//---------------------------------------------------------
	virtual void Draw() = 0;
};